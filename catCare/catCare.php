            <h4>Litter Box</h4>
            <p>
                All indoor cats need a litter box, which should be placed in a quiet, accessible location. In a
                multi-level home, one box per floor is recommended. Avoid moving the box unless absolutely necessary,
                but if you must do so, move the box just a few inches per day. Keep in mind that cats won't use a messy,
                smelly litter box, so scoop solid wastes out of the box at least once a day. Dump everything, wash with
                a mild detergent and refill at least once a week; you can do this less frequently if using clumping
                litter. Don't use ammonia, deodorants or scents, especially lemon, when cleaning the litter box. If your
                cat will not use a litterbox, please consult with your veterinarian. Sometimes refusal to use a litter
                box is based on a medical condition that required treatment. Please visit our Litter Box Problems page
